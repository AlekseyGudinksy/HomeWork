package bsu.tat.Route;

/**
 * Class which contains fields for ArrayList point
 */
public class Point {
    private double x = 0;
    private double y = 0;

    /**
     * @param x
     * @param y
     */
    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    /**
     * @return x
     */
    public double getX() {

        return x;
    }

    /**
     * @return y
     */
    public double getY() {

        return y;
    }
}
